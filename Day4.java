package OneHundredDaysOfCoding;
public class Day4 {
    public static void main(String[] args) {
        /*  
        Escape Sequence
        \n = Newline
        \t = Tab horizontal
        \b = Backspace
        \r = Carriage return
        \' = Karakter kutip 1/tunggal
        \" = Karakter kutip 2/ganda
        \\ = Karakter backslash
        */  
        System.out.println("ini contoh newline \nini Newline");
        System.out.println("ini contoh \tTab");
        System.out.println("ini contoh backspace, ini\b Backspace");
        System.out.println("ini contoh \rCarriage Returm");
        System.out.println("ini contoh \'kutip Tunggal\'");
        System.out.println("ini contoh \"kutip Ganda\"");
        System.out.println("ini contoh \\backslash\\");
    }
}
