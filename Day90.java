package OneHundredDaysOfCoding;
public class Day90 {
    public static void main(String[] args) {
        int angka = 7;
        int hasil = kuadrat(angka);
        System.out.println("Kuadrat dari " + angka + " adalah " + hasil);
    }
    public static int kuadrat(int x) {
        return x * x;
    }
}
