package OneHundredDaysOfCoding;
public class Day9 {
    public static void main(String[] args) {
        int angka; // hanya mendeklarasikan variabel bernama 'angka' dengan tipe data int

        angka = 10; // memberi nilai awal (inisialisasi) pada variabel 'angka'
        System.out.println("Nilai awal angka: " + angka);

        angka = 25; // memperbarui nilai variabel 'angka'
        System.out.println("Nilai setelah update: " + angka);

        String nama = "Rayden"; // deklarasi sekaligus inisialisasi
        System.out.println("Nama: " + nama);

        nama = "Budi"; // update nilai variabel
        System.out.println("Nama setelah update: " + nama);
    }
}
