package OneHundredDaysOfCoding;
public class Day19 {
    public static void main(String[] args) {
        double a = 65;
        System.out.println("Double\t: "+a);
        int b = (int) a;
        System.out.println("Integer\t: "+b);
        char c = (char) b;
        System.out.println("Char\t: "+c);
        byte d = (byte) c;
        System.out.println("Byte\t: "+d);
    }
}
